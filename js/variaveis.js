//#region var
/**
 * var - elas podem ser recriadas com o mesmo nome
 * e tambem podem ser alteras seus valores
 */
// criando
var meuNome = "felipe de oliveira ribeiro"
console.log(meuNome)

// alterando valor
meuNome = 'Felipe de Oliveira Ribeiro'
console.log(meuNome)

// recriando a variavel
var meuNome = 'Felipe de Oliveira'
console.log(meuNome)
//#endregion

//#region let
/**
 * let - elas podem ser alteras seus valores
 * mas não podem ser recriadas
 */
// criando
let idade = 26
console.log(idade)

// alterando
idade = 25
console.log(idade)

//#endregion

//#region const
/**
 * const - elas não podem ser recriadas com o mesmo nome
 * e tambem não podem ser alteras seus valores
 */
const email = 'felipeoliveirasl@hotmail.com'
console.log(email)
//#endregion
